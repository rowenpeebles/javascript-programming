// Implicit Conversion to String

let result;

// numeric string used with '+' gives a string type

result = '3' + 2
console.log(result);
// = 32

result = '3' + true;
console.log(result);
// = 3+true

result = '3' + undefined;
console.log(result);
// = 3undefined

result = '3' + null
console.log(result);
// = 3null
