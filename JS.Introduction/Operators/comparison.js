// Comparison Operators in JavaScript

// Equal operator
console.log(2 == 2);
console.log(2 == '2');

// Not equal operator
console.log(3 != 2);
console.log('hello' != 'Hello');

// Strict equal operator
console.log(2 === 2);
console.log(2 === '2');

// Strict not equal operator
console.log(2 !== '2');
console.log(2 !== 2);
