// Arithmetic Operators in JavaScript

let x = 5;
let y = 3;

// Addition
console.log('x + y = ', x + y);

// Subtraction
console.log('x - y = ', x - y);

// Multiplication
console.log('x * y = ', x * y);

// Division
console.log('x / y = ', x / y);

// Remainder 
console.log('x % y = ', x % y);

// Increment
console.log('++x = ', ++x);
console.log('x++ = ', x++);
console.log('x = ', x);

// Decrement
console.log('--x = ', --x);
console.log('x-- = ', x--);
console.log('x = ', x);

// Exponentiation
console.log('x ** y = ', x ** y);
